-- Удаление полей цены и изображения из таблицы витаминов
-- Витамины теперь представляют категории, а не конкретные товары

ALTER TABLE vitamins DROP COLUMN IF EXISTS price;
ALTER TABLE vitamins DROP COLUMN IF EXISTS image_url;

-- Обновляем существующие записи, убираем цены
UPDATE vitamins SET 
    description = CASE 
        WHEN name = 'Витамин D3' THEN 'Поддерживает иммунную систему, здоровье костей и улучшает настроение'
        WHEN name = 'Магний' THEN 'Помогает справиться со стрессом, улучшает сон и поддерживает работу мышц'
        WHEN name = 'B-комплекс' THEN 'Поддерживает энергетический обмен, нервную систему и метаболизм'
        WHEN name = 'Омега-3' THEN 'Поддерживает здоровье сердца, мозга и помогает снизить воспаление'
        WHEN name = 'Цинк' THEN 'Укрепляет иммунитет, улучшает состояние кожи и ускоряет заживление'
        WHEN name = 'Железо' THEN 'Предотвращает анемию, повышает энергию и улучшает транспорт кислорода'
        WHEN name = 'Коэнзим Q10' THEN 'Поддерживает энергию клеток, здоровье сердца и действует как антиоксидант'
        WHEN name = 'Пробиотики' THEN 'Поддерживают здоровье кишечника, укрепляют иммунитет и нормализуют микрофлору'
        ELSE description
    END;
